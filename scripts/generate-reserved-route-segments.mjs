import { readFile, writeFile } from "node:fs/promises";

const routeTreePath = new URL("../src/routeTree.gen.ts", import.meta.url);
const outputPath = new URL(
  "../lib/reserved-top-level-route-segments.ts",
  import.meta.url,
);

const routeTreeSource = await readFile(routeTreePath, "utf8");
const fullPathBlock = routeTreeSource.match(
  /export interface FileRoutesByFullPath \{(?<body>[\s\S]*?)\n\}/,
);

if (!fullPathBlock?.groups?.["body"]) {
  throw new Error("Could not find FileRoutesByFullPath in routeTree.gen.ts");
}

const routePaths = [...fullPathBlock.groups["body"].matchAll(/'([^']+)'/g)].map(
  (match) => match[1],
);
const reservedSegments = routePaths
  .flatMap((routePath) => {
    if (!routePath) {
      return [];
    }
    const segments = routePath.split("/").filter(Boolean);
    if (segments.length !== 1) {
      return [];
    }
    const [segment] = segments;
    if (!segment || segment.startsWith("$") || segment.includes("{")) {
      return [];
    }
    return [segment];
  })
  .toSorted((left, right) => left.localeCompare(right));

const reservedSegmentSource =
  reservedSegments.length === 0
    ? "[]"
    : `[\n${reservedSegments
        .map((segment) => `  ${JSON.stringify(segment)},`)
        .join("\n")}\n]`;

const fileContents = `// Generated by scripts/generate-reserved-route-segments.mjs. Do not edit manually.
export const RESERVED_TOP_LEVEL_ROUTE_SEGMENTS = ${reservedSegmentSource} as const;
`;

await writeFile(outputPath, fileContents);
